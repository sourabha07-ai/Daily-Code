#include <iostream>
using namespace std;

int main(){
    int num1 = 20;
    int num2 = 21;
//Comparision operator (<,>,>=,<=,==,!=)
    cout <<(num1>num2) <<endl;//false-0
    cout <<(num1<num2) <<endl;//true-1
    cout <<(num1==num2) <<endl;//false-0
    cout<<(num1!=num2) <<endl;//true-1
    cout<<(num1>=num2) <<endl;//false-0
    cout <<(num1 <= num2) <<endl;//true-1


    return 0;
}